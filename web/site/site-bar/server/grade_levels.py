def band_for(grade: str):
    """(нижний, верхний, название, инструкции) для класса; None — если класс не распознан."""
    m = re.search(r"\d+", str(grade or ""))
    if not m:
        return None
    g = int(m.group(0))
    for lo, hi, name, rules in _BANDS:
        if lo <= g <= hi:
            return lo, hi, name, rules
    return None


def profile_for(grade: str) -> str:
    """Блок инструкций по возрастной подаче для промпта. Класс не указан → ""."""
    band = band_for(grade)
    if not band:
        return ""
    _lo, _hi, name, rules = band
    return (f"ВОЗРАСТНАЯ ПОДАЧА — {name}.\n"
            f"Материал для учителя может быть глубоким, но всё, что адресовано ДЕТЯМ "
            f"(раздел «Что рассказать детям», вопросы, формулировки опыта), строго "
            f"соответствует этой ступени:\n{rules.strip()}")
